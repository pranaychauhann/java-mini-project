    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        try {
           con.setAutoCommit(false);
             String accno =  txtaccno.getText();
             String cust_id =  jLabel7.getText();
             String date =  jLabel6.getText();
            String balance =  lbal.getText();
            String amount1 =  amount.getText();
            
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/bankmgmt","root","");
            String queryco1 = "Insert into deposit(acc_id,customer_id,date,balance,deposit)values(?,?,?,?,?)";  
            insert = con.prepareStatement(queryco1);
            insert.setString(1,accno);
            insert.setString(2,cust_id);
            insert.setString(3,date);
            insert.setString(4,balance);
            insert.setString(5,amount1);
            insert.executeUpdate();
          
             
             String sqlupdate= "update acc_details set balance= balance+ ? where acc_id=? ";   
             update=con.prepareStatement(sqlupdate);
             update.setString(1, amount1);    
             update.setString(2, accno);
            
             update.executeUpdate(); 
              JOptionPane.showMessageDialog(null,"Amount Deposited Successfully");
            con.commit();
            
        }
        
        catch (SQLException ex) {
            try {
                Logger.getLogger(deposit.class.getName()).log(Level.SEVERE, null, ex);
                con.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(deposit.class.getName()).log(Level.SEVERE, null, ex1);
            }
        
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(deposit.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

